package FirstJavaProject;

public class MathFunctions {
    /**
     * Addition with parameters x and y.
     * Returns result as int.
     */
    public static int addition(int x, int y) {
        return x + y;
    }

    /**
     * Multiplication with parameters x and y.
     * Returns result as int.
     */
    public static int multiplication(int x, int y) {
        return x * y;
    }

    /**
     * Subraction with parameters x and y.
     * Returns result as int.
     */
    public static int subraction(int x, int y) {
        return x - y;
    }

    /**
     * Division with parameters x and y.
     * Returns result as int.
     */
    public static int division(int x, int y) {
        return x / y;
    }

    /**
     * Modulus with parameters x and y.
     * Returns result as int.
     */
    public static int modulus(int x, int y) {
        return x % y;
    }

}
